################################################################################
# display queue manager aliases
################################################################################
sub disQmgrAlias
{
  my $rd    = $_[0];
  my $wr    = $_[1];
  my $parse = $_[2];
  my $os    = $_[3];

  print $wr "display qremote($parse) where ( rname eq '' )\n" ;
  print $wr "ping qmgr\n" if $os eq 'UNIX' ;

  return parseMqsc $rd, $os ;
}

